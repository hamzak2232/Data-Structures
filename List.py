List = ["Bruce", "Wayne", "Batman"]
print("\nList containing multiple values: ")
print(List)

List2 = [['Bruce', 'Wayne'], ['Batman']]
print("\nMulti-Dimensional List: ")
print(List2)

print("Accessing element from the list")
print(List[0])
print(List[2])

print("Accessing element using negative indexing")

print(List[-1])

print(List[-3])

print("Accessing element from the multi-dimensional list")
print(List2[0][0])
print(List2[0][1])
print(List2[1][0])

print("Accessing element using negative indexing")
print(List2[0][-1])
print(List2[0][-2])
print(List2[-1][-1])